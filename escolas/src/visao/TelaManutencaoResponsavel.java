    private void TabelaResponsavelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaResponsavelMouseClicked
        // TODO add your handling code here:
        
        int posicao =  TabelaResponsavel.getSelectedRow();
        Responsavel r = listaResponsavel.get(posicao); 
       txtSiape.setText(String.valueOf(r.getSiape()));
        txtNome.setText(String.valueOf(r.getNome()));   
        txtDepartamento.setText(r.getDepartamento());
        txtFuncao.setText(r.getFuncao());
    }//GEN-LAST:event_TabelaResponsavelMouseClicked

    private void alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarActionPerformed
        // TODO add your handling code here:
        int nrItem = TabelaResponsavel.getSelectedRow();
        
        if(nrItem == -1){
        JOptionPane.showInputDialog(this,"Selecione uma linha");
        }
        
        else{
        Responsavel r= new Responsavel();
        r.setSiape(Integer.valueOf(txtSiape.getText()));
        r.setNome(txtNome.getText());
        r.setDepartamento(txtDepartamento.getText());
        r.setFuncao(txtFuncao.getText());
        controleResponsavel.alterar(r);
        atualizaTabela(true);
        }
        
        
        
    }//GEN-LAST:event_alterarActionPerformed

