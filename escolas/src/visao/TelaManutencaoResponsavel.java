     public void atualizaTabela(boolean flag){
    DefaultTableModel modelo = (DefaultTableModel) TabelaResponsavel.getModel();
    if(flag){
    listaResponsavel.clear();
    listaResponsavel.addAll(controleResponsavel.listarTodos());
    }
    
    while (modelo.getRowCount()>0){
    
        modelo.removeRow(0);
    
    }
    
    for(Responsavel r: listaResponsavel){
    modelo.addRow(new Object[] {r.getSiape(), r.getNome(), r.getDepartamento(), r.getFuncao() });
    }
    
    }
    
