    public void atualizaTabela(boolean flag){
    DefaultTableModel modelo = (DefaultTableModel) TabelaSalas.getModel();
    if(flag){
    listaSalas.clear();
    listaSalas.addAll(controlesala.listarTodos());
    }
    
    while (modelo.getRowCount()>0){
    
        modelo.removeRow(0);
    
    }
    
    for(salas s: listaSalas){
    modelo.addRow(new Object[] {s.getNumero(), s.getDescricao(), s.getBloco() });
    }
    
    }
    
    
