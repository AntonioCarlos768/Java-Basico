     private void atualizaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) TabelaCliente.getModel();

        lista.addAll(cc.listarTodos());
        modelo.setNumRows(0);

        for (Cliente dado : lista) {
            modelo.addRow(new Object[]{dado.getNome()});
        }
    }
    
