     private void atualizaTabela()
     {
        // pega o modelo da tabela, que foi definido para 
        // a inclusÃ£o dos dados
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        
        // enquanto houver linhas na tabela
        lista.clear();
        lista.addAll(contResp.listarTodas());
     
        while(modelo.getRowCount()>0)
        {
            modelo.removeRow(0);
        }
        
        
        // percorre o ArrayList de salas
        for(Responsavel s: lista)
        {
           modelo.addRow(new Object[]{s.getNome()});
        }
    }
     
