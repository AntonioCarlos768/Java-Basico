    private void botInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botInserirActionPerformed
        
        // criando um objeto da classe Patrimonio
        Patrimonio p = new Patrimonio();
        
        // preenchendo os atributos do objeto 
        p.setNumero(Integer.parseInt(txtNumPatrimonio.getText()));
        p.setValor(Double.parseDouble(txtValor.getText()));
        p.setDescricao(txtDescricao.getText());
        p.setDataaquisicao(dataAquisicao.getDate());
        p.setDataDesfazimento(dataDesfazimento.getDate());
        
        // coloca o item selecionado da caixa de combinação
        // na sala do objeto
        p.setSala((Sala) comboSala.getSelectedItem());
        p.setResponsavel(this.respSelecionado);
               
        // adicionando o objeto à lista (model)
        listaPatrimonio.addElement(p);
        contPatrimonio.inserir(p);
        limparCampos();
    }//GEN-LAST:event_botInserirActionPerformed

    private void limparCampos()
    {
        // limpando os campos
        txtValor.setText("");
        txtNumPatrimonio.setText("");
        txtDescricao.setText("");    
        dataAquisicao.setDate(null);
        dataDesfazimento.setDate(null);  
        txtNomeResponsavel.setText("");
    }
    
    private void botRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botRemoverActionPerformed
        
        // pegar a linha selecionada pelo usuário
        int linha = ListBoxPatrimonio.getSelectedIndex();
        
        // verificar se foi selecionada uma linha
        // -1 indica que não há linhas selecionadas
        if (linha == -1)
        {
            // apresenta uma mensagem ao usuário, indicando que
            // ele não selecionou nenhum patrimonio para exclusão
            JOptionPane.showMessageDialog(this, 
                        "Nenhum bem patrimoniado foi selecionado");
        }
        else
        {
            // ocorre quando o usuário seleciona uma linha no listbox
            
            // remove o elemento que foi selecionado   
            contPatrimonio.excluir(listaPatrimonio.get(linha));
            listaPatrimonio.remove(linha);

            limparCampos();
        }
        
        
    }//GEN-LAST:event_botRemoverActionPerformed

    private void ListBoxPatrimonioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListBoxPatrimonioMouseClicked
               
        // pega a posicao selecionada no listbox
        int posicao = ListBoxPatrimonio.getSelectedIndex();
        
        // pega o patrimonio  na lista de patrimonios
        Patrimonio p = listaPatrimonio.elementAt(posicao);
        this.respSelecionado=p.getResponsavel();
        
        // colocar os atributos na tela
        txtNumPatrimonio.setText(String.valueOf(p.getNumero()));
        txtValor.setText(String.valueOf(p.getValor()));
        txtDescricao.setText(p.getDescricao());
        dataAquisicao.setDate(p.getDataaquisicao());
        dataDesfazimento.setDate(p.getDataDesfazimento());
        txtNomeResponsavel.setText(p.getResponsavel().getNome());
        
        // define o combo como sendo a sala do objeto selecionado
        comboSala.setSelectedItem(p.getSala());
        
    }//GEN-LAST:event_ListBoxPatrimonioMouseClicked

