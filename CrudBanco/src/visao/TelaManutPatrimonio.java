    private void botAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botAlterarActionPerformed

        // pega o número do item selecionado na listbox
        int nrItem = ListBoxPatrimonio.getSelectedIndex();
        
        // verificar se foi selecionado um item
        if (nrItem == -1)
        {
            // não foi selecionado nenhum item da lista
            // apresentar uma mensagem
            JOptionPane.showMessageDialog(this, "Um bem patrimoniado deve ser selecionada");           
        }
        else
        {
            // um item foi selecionado na lista
            
            // cria um novo objeto para sobrepor o antigo
            Patrimonio p = new Patrimonio();
            
            // preencher o objeto com os dados da interface
            p.setNumero(Integer.parseInt(txtNumPatrimonio.getText()));
            p.setValor(Double.parseDouble(txtValor.getText()));
            p.setDescricao(txtDescricao.getText());
            p.setDataaquisicao(dataAquisicao.getDate());
            p.setDataDesfazimento(dataDesfazimento.getDate());
            p.setSala((Sala) comboSala.getSelectedItem());
            p.setResponsavel(this.respSelecionado);
            
            // atualizar o elemento da posicao selecionada
            listaPatrimonio.setElementAt(p, nrItem);
            contPatrimonio.alterar(p);
            
            limparCampos();
        }
    }//GEN-LAST:event_botAlterarActionPerformed

