    public void setAluno(Aluno aluno) {

        txtra.setText(String.valueOf(aluno.getRa()));
        txtnome.setText(aluno.getNome());
        txtProfessor.setText(aluno.getProfessor().getNome());
        txtCurso.setText(aluno.getCurso().getNome());
        txtendereco.setText(aluno.getEndereco());
        txtcidade.setText(aluno.getCidade());
        txtcep.setText(aluno.getCep());
        txtuf.setText(aluno.getUF());

        professor = aluno.getProfessor();
    }

    public boolean isSalvar() {
        return salvar;
    }

    public void setSalvar(boolean salvar) {

        aluno.setNome(txtnome.getText());
        aluno.setProfessor(professor);
        aluno.setCurso(curso);
        aluno.setRa(txtra.getText());
        aluno.setEndereco(txtendereco.getText());
        aluno.setCidade(txtcidade.getText());
        aluno.setCep(txtcep.getText());
        aluno.setUF(txtuf.getText());
        this.salvar = salvar;
    }

