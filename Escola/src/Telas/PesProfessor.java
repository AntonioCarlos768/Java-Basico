    private void atualizaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaAluno.getModel();

        lista.addAll(ct.listarTodos());
        modelo.setNumRows(0);

        for (Professor dado : lista) {
            modelo.addRow(new Object[]{dado.getNome()});
        }
    }
    
    
