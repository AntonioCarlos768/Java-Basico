     private void atualizaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) TabelaCurso.getModel();

        lista.addAll(cc.listarTodos());
        modelo.setNumRows(0);

        for (Curso dado : lista) {
            modelo.addRow(new Object[]{dado.getNome()});
        }
    }
    
    
